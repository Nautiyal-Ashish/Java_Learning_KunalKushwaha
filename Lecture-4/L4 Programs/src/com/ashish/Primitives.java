package com.ashish;

public class Primitives {
    public static void main(String[] args) {
// Primitive - Any data type we can not break further
        // Integer -> 4 bytes
        int rollNo = 64;

        //Character -> 8 bytes
        char letter = 'r';

        //Float
        float marks = 98.55f;

        //Double
        double largeDecimalsNumbers = 45441545454.1151;

        // Long -> 8 bytes
        long largeInteger = 65465454654654654L;

        //Boolean
        boolean bool = true;

        // String is not primitive
        String name = "Ashish";



    }
}
